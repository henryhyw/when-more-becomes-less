# Figure 2 — adjacent / displaced (5 reps) / 13-model heatmap
# ===========================================================================
def fig2_main_curves():
    disp = pd.read_parquet(SUMMARY / "displaced_all.parquet")
    fig, axes = plt.subplots(1, 3, figsize=(20.0, 5.0),
                             gridspec_kw=dict(width_ratios=[1.70, 1.70, 1.40]))

    # (a) Adjacent baseline
    ax = axes[0]
    adj_files = sorted((DATA / "adjacent").glob("adjacent__*.parquet"))
    adj = (pd.concat([pd.read_parquet(f) for f in adj_files], ignore_index=True)
           if adj_files else pd.DataFrame())
    if not adj.empty:
        for mid in [m for m, _, _, _ in PANEL if m in adj["model"].unique()]:
            short, fam, color = ID_TO[mid]
            sub = adj[adj.model == mid]
            curve = sub.groupby("N")["target_prob"].mean().sort_index()
            ax.plot(curve.index, curve.values, marker="o",
                    label=f"{short} ({fam})",
                    linestyle="-" if fam == "MLM" else "--",
                    color=color, linewidth=2.2)
    ax.set_xlabel("repetitions in prefix $N$")
    ax.set_ylabel("mean P(target)")
    ax.set_title("(a) Adjacent baseline: monotonic priming")
    ax.legend(loc="lower right", framealpha=0.92, fontsize=9)
    ax.grid(True, alpha=0.3)

    # (b) Displaced F0 — 5 representative models
    ax = axes[1]
    for mid, short, fam, color, ls in FIG2B_REPS:
        sub = disp[(disp.model == mid) & (disp.frame_id == "F0")]
        if sub.empty or sub["target_prob"].isna().all():
            continue
        curve = sub.groupby("N")["target_prob"].mean().sort_index()
        ax.plot(curve.index, curve.values, marker="o",
                label=f"{short} ({fam})",
                linestyle=ls, color=color, linewidth=2.2, markersize=5)
    ax.set_xlabel("repetitions in prefix $N$")
    ax.set_ylabel("mean P(target)")
    ax.set_title("(b) Displaced probe F0 (5 representative models)")
    ax.legend(loc="upper right", fontsize=9, framealpha=0.92)
    ax.grid(True, alpha=0.3)

    # (c) Heatmap across all 13 panel models
    ax = axes[2]
    rows = []
    NS_NZ = [1, 3, 5, 10, 20, 30]
    for mid, short, fam, color in PANEL:
        sub = disp[(disp.model == mid) & (disp.frame_id == "F0")]
        if sub.empty or sub["target_prob"].isna().all():
            continue
        curve = sub.groupby("N")["target_prob"].mean()
        vals = np.array([curve.get(n, np.nan) for n in NS_NZ])
        peak = np.nanmax(vals)
        if peak is None or peak <= 0 or np.isnan(peak):
            continue
        norm = vals / peak
        p30 = vals[-1] if not np.isnan(vals[-1]) else 0.0
        drop = (peak - p30) / peak if peak > 0 else 0.0
        rows.append({"model": short, "fam": fam, "drop": drop, "norm": norm,
                     "peak_n": NS_NZ[int(np.nanargmax(vals))]})
    rows = sorted(rows, key=lambda r: -r["drop"])
    M = np.array([r["norm"] for r in rows])
    labels = [f"{r['model']} ({r['fam']})" for r in rows]
    im = ax.imshow(M, aspect="auto", cmap="magma", vmin=0.0, vmax=1.0,
                   extent=[-0.5, len(NS_NZ) - 0.5, len(rows) - 0.5, -0.5])
    ax.set_xticks(range(len(NS_NZ)))
    ax.set_xticklabels([str(n) for n in NS_NZ])
    ax.set_yticks(range(len(rows)))
    ax.set_yticklabels(labels, fontsize=8.5)
    ax.set_xlabel("$N$ (repetitions)")
    ax.set_title("(c) Peak-normalised P(target) across all 13 panel models")
    for i, r in enumerate(rows):
        peak_idx = NS_NZ.index(r["peak_n"])
        ax.plot(peak_idx, i, marker="o", color="white", markersize=4.0,
                markeredgecolor="black", markeredgewidth=0.7)
    cbar = fig.colorbar(im, ax=ax, fraction=0.04, pad=0.02)
    cbar.set_label("P(target) / per-curve peak", fontsize=9)
    cbar.ax.tick_params(labelsize=8)

    fig.tight_layout()
    fig.savefig(FIGS / "fig2_main_curves.png", dpi=160, bbox_inches="tight")
    fig.savefig(FIGS / "fig2_main_curves.pdf", bbox_inches="tight")
    plt.close(fig)
    print(f"  fig2 → {FIGS / 'fig2_main_curves.png'}")


