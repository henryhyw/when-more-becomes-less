def fig3_crosslingual():
    src = MULTILING / "summary" / "model_lang_frame_summary.csv"
    if not src.exists():
        print(f"  no multilingual summary at {src}; skipping fig3"); return
    df = pd.read_csv(src)

    # Row order: MLM block first (XLM-R-base, large), then CLM block (Qwen 1.5B, 7B).
    model_order = ["xlm-roberta-base", "xlm-roberta-large",
                   "Qwen2.5-1.5B", "Qwen2.5-7B"]
    model_labels = {"xlm-roberta-base":  "XLM-R-base (MLM)",
                    "xlm-roberta-large": "XLM-R-large (MLM)",
                    "Qwen2.5-1.5B":      "Qwen2.5-1.5B (CLM)",
                    "Qwen2.5-7B":        "Qwen2.5-7B (CLM)"}

    # Column order: Spanish, German, French, Chinese.
    lang_order = ["es", "de", "fr", "zh"]
    lang_labels = {"es": "Spanish", "de": "German", "fr": "French",
                   "zh": "Chinese"}

    frames = ["F0", "F1", "F2"]

    fig, axes = plt.subplots(1, 3, figsize=(13.5, 3.6),
                             gridspec_kw=dict(width_ratios=[1, 1, 1.1]))

    # Warm sequential colormap: light cream → deep accent.
    cmap = LinearSegmentedColormap.from_list(
        "cl_drop", [(1.00, 0.95, 0.86),
                    (0.95, 0.75, 0.55),
                    (0.83, 0.47, 0.25),
                    (0.55, 0.20, 0.10)])
    vmin, vmax = 0.0, 0.9

    last_im = None
    for ax, frame in zip(axes, frames):
        sub = df[df.frame_id == frame].copy()
        M = np.full((len(model_order), len(lang_order)), np.nan)
        for i, mid in enumerate(model_order):
            for j, lang in enumerate(lang_order):
                row = sub[(sub.model == mid) & (sub.lang == lang)]
                if not row.empty:
                    M[i, j] = float(row["drop_mean"].values[0])
        last_im = ax.imshow(M, aspect="auto", cmap=cmap, vmin=vmin, vmax=vmax)

        # Annotate each cell.
        for i in range(len(model_order)):
            for j in range(len(lang_order)):
                v = M[i, j]
                if np.isnan(v):
                    ax.text(j, i, "n/a", ha="center", va="center",
                            fontsize=8.5, color="#777")
                else:
                    txt_color = "white" if v > 0.55 else "#2a2a28"
                    ax.text(j, i, f"{v:.2f}", ha="center", va="center",
                            fontsize=9, color=txt_color, fontweight="medium")

        ax.set_xticks(range(len(lang_order)))
        ax.set_xticklabels([lang_labels[l] for l in lang_order],
                           fontsize=9.5)
        if ax is axes[0]:
            ax.set_yticks(range(len(model_order)))
            ax.set_yticklabels([model_labels[m] for m in model_order],
                               fontsize=9.5)
        else:
            ax.set_yticks([])
        ax.set_title(f"({chr(ord('a') + frames.index(frame))}) {frame}",
                     fontsize=11)
        # Light gridlines between cells.
        ax.set_xticks(np.arange(-0.5, len(lang_order), 1), minor=True)
        ax.set_yticks(np.arange(-0.5, len(model_order), 1), minor=True)
        ax.grid(which="minor", color="white", linestyle="-", linewidth=1)
        ax.tick_params(which="minor", length=0)

    if last_im is not None:
        cbar = fig.colorbar(last_im, ax=axes[-1], fraction=0.045, pad=0.04)
        cbar.set_label("per-word drop", fontsize=9.5)
        cbar.ax.tick_params(labelsize=8.5)

    fig.tight_layout()
    fig.savefig(FIGS / "fig3_crosslingual.png", dpi=160, bbox_inches="tight")
