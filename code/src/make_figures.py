# Figure 4 — six-condition causal ablation
# ===========================================================================
def fig4_ablation():
    abl_files = sorted((DATA / "ablation").glob("ablation__*.parquet"))
    if not abl_files:
        print("  no ablation files; skipping fig3"); return
    abl = pd.concat([pd.read_parquet(f) for f in abl_files], ignore_index=True)
    cond_order = ["full_repeat_N30", "truncate_to_N3", "delete_repeat_block",
                  "random_filler_same_length", "unique_semantic_filler",
                  "repeat_unrelated_N30"]
    cond_labels = ["Full N=30", "Trunc. N=3", "Deleted",
                   "Random", "Semantic", "Table×30"]

    models = [(m, s, f, c) for (m, s, f, c) in MECH_MODELS if m in abl["model"].unique()]
    fig, axes = plt.subplots(1, 3, figsize=(15.4, 5.0))
    width = 0.8 / max(1, len(models))
    x = np.arange(len(cond_order))

    # (a) target prob with CIs
    ax = axes[0]
    for i, (mid, short, fam, color) in enumerate(models):
        sub = abl[abl.model == mid]
        means, los, his = [], [], []
        for c in cond_order:
            v = sub[sub.condition == c]["target_prob"].dropna().values
            mn, lo, hi = boot_ci(v, n=300, seed=42)
            means.append(mn); los.append(lo); his.append(hi)
        means, los, his = np.array(means), np.array(los), np.array(his)
        offs = (i - len(models) / 2 + 0.5) * width
        ax.bar(x + offs, means, width=width,
               yerr=[np.nan_to_num(means - los, nan=0.0),
                     np.nan_to_num(his - means, nan=0.0)],
               capsize=2.5, label=short, color=color)
    ax.set_xticks(x); ax.set_xticklabels(cond_labels, rotation=22, ha="right")
    ax.set_ylabel("mean P(target) (95% CI)")
    ax.set_title("(a) Target probability")
    ax.grid(True, alpha=0.3, axis="y")
    ax.set_xlim(-0.5, len(cond_order) - 0.5)

    # (b) ΣP(synonyms)
    ax = axes[1]
    for i, (mid, short, fam, color) in enumerate(models):
        sub = abl[abl.model == mid]
        means = []
        for c in cond_order:
            v = sub[sub.condition == c]["sum_synonym_prob"].dropna().values
            means.append(float(np.mean(v)) if len(v) else float("nan"))
        offs = (i - len(models) / 2 + 0.5) * width
        ax.bar(x + offs, means, width=width, color=color)
    ax.set_xticks(x); ax.set_xticklabels(cond_labels, rotation=22, ha="right")
    ax.set_ylabel(r"mean $\Sigma\,P(\mathrm{synonyms})$")
    ax.set_title("(b) Semantic-field mass")
    ax.grid(True, alpha=0.3, axis="y")
    ax.set_xlim(-0.5, len(cond_order) - 0.5)

    # (c) target rank (log)
    ax = axes[2]
    for i, (mid, short, fam, color) in enumerate(models):
        sub = abl[abl.model == mid]
        means = []
        for c in cond_order:
            v = sub[sub.condition == c]["target_rank"].dropna().values
            v = v[v > 0]
            means.append(float(np.mean(v)) if len(v) else float("nan"))
        offs = (i - len(models) / 2 + 0.5) * width
        ax.bar(x + offs, means, width=width, color=color)
    ax.set_xticks(x); ax.set_xticklabels(cond_labels, rotation=22, ha="right")
    ax.set_yscale("log")
    ax.set_ylabel("mean target rank (log)")
    ax.set_title("(c) Target rank (lower = better)")
    ax.grid(True, alpha=0.3, axis="y", which="both")
    ax.set_xlim(-0.5, len(cond_order) - 0.5)

    legend_handles = [plt.Rectangle((0, 0), 1, 1, color=c) for (_, _, _, c) in models]
    legend_labels = [s for (_, s, _, _) in models]
    fig.legend(legend_handles, legend_labels, loc="lower center",
               ncol=len(models), fontsize=10, frameon=True, framealpha=0.92,
               bbox_to_anchor=(0.5, -0.04))
    fig.tight_layout(rect=[0, 0.04, 1, 1])
    fig.savefig(FIGS / "fig4_ablation.png", dpi=160, bbox_inches="tight")
    fig.savefig(FIGS / "fig4_ablation.pdf", bbox_inches="tight")
    plt.close(fig)
    print(f"  fig4 → {FIGS / 'fig4_ablation.png'}")

    # Export contrast table (truncate − full)
    rows = []
    for mid, short, fam, color in models:
        sub = abl[abl.model == mid]
        full = sub[sub.condition == "full_repeat_N30"].set_index("target_word")["target_prob"]
        trunc = sub[sub.condition == "truncate_to_N3"].set_index("target_word")["target_prob"]
        common = full.index.intersection(trunc.index)
        d = (trunc.loc[common] - full.loc[common]).dropna().values
        if len(d) == 0:
            continue
        rng2 = np.random.default_rng(42)
        boots = [d[rng2.integers(0, len(d), size=len(d))].mean() for _ in range(500)]
        lo, hi = float(np.percentile(boots, 2.5)), float(np.percentile(boots, 97.5))
        full_r = sub[sub.condition == "full_repeat_N30"].set_index("target_word")["target_rank"].mean()
        trunc_r = sub[sub.condition == "truncate_to_N3"].set_index("target_word")["target_rank"].mean()
        rows.append({"model": mid, "model_short": short, "n_words": int(len(d)),
                     "mean_diff": float(d.mean()), "ci_lo": lo, "ci_hi": hi,
                     "rank_full": float(full_r), "rank_trunc": float(trunc_r)})
    pd.DataFrame(rows).to_csv(SUMMARY / "ablation_contrasts.csv", index=False)


