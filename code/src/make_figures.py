# Figure (Appendix) layerwise — per-layer attention vs 1/N reference
# ===========================================================================
def _per_layer_attention_ratio(df_attn):
    """Return DataFrame indexed by (layer, N) with log2(per_token / 1/N reference)."""
    g = (df_attn.groupby(["layer", "N"])
         .agg(per_token=("attn_per_target_token", "mean"),
              total=("attn_total_to_target_block", "mean"))
         .reset_index())
    refs = g[g.N == g.N.min()].set_index("layer")["per_token"]
    g["ref"] = g.apply(lambda r: refs.get(r.layer, np.nan) * (g.N.min() / r.N), axis=1)
    g["log2_ratio"] = np.log2(g["per_token"].clip(lower=1e-9) / g["ref"].clip(lower=1e-9))
    return g


def fig6_layerwise():
    NS_LW = [1, 3, 5, 10, 20, 30]
    LW_PANEL = [(m, s, f) for (m, s, f, _) in MECH_MODELS]

    fig, axes = plt.subplots(1, 4, figsize=(15.4, 4.6),
                             gridspec_kw=dict(width_ratios=[1, 1, 1, 1.07]))
    # Diverging cmap with luminance-asymmetric endpoints so super-dilution
    # (dark purple) and sub-dilution (orange) remain distinguishable in
    # grayscale. Purple Y≈0.28 vs orange Y≈0.60.
    cmap = LinearSegmentedColormap.from_list(
        "po", [(0.34, 0.20, 0.54), (1.0, 1.0, 1.0), (1.0, 0.50, 0.07)])

    last_im = None
    for ax, (mid, short, fam) in zip(axes, LW_PANEL):
        f = DATA / "layerwise" / f"layerwise__{_safe(mid)}.parquet"
        if not f.exists():
            ax.set_title(f"{short}: missing"); continue
        df = pd.read_parquet(f)
        df = df[df.metric == "attention"]
        df = df[(df.N > 0) & (df.n_target_positions > 0)]
        df = df.dropna(subset=["attn_per_target_token", "layer"])
        df["layer"] = df["layer"].astype(int)
        if df.empty:
            ax.set_title(f"{short}: no attn data"); continue

        g = _per_layer_attention_ratio(df)
        H = g.pivot_table(index="layer", columns="N", values="log2_ratio", aggfunc="mean")
        cols = [n for n in NS_LW if n in H.columns]
        # Drop the anchor column: by construction log2_ratio at N=min is
        # identically 0 (per-token attn equals its own reference), so the
        # leftmost column is structurally white and carries no information.
        if cols:
            cols = cols[1:]
        if not cols or H.empty:
            ax.set_title(f"{short}: empty"); continue
        H = H[cols].sort_index()
        Hv = np.clip(np.nan_to_num(H.values, nan=0.0), -3, 3)
        n_layers = int(H.index.max())
        im = ax.imshow(Hv, aspect="auto", cmap=cmap, vmin=-3, vmax=3,
                       extent=[-0.5, len(cols) - 0.5,
                               n_layers + 0.5, int(H.index.min()) - 0.5])
        ax.set_xticks(range(len(cols)))
        ax.set_xticklabels([str(n) for n in cols])
        ax.set_xlabel("$N$")
        ax.set_title(f"{short}\n({fam}, {n_layers} layers)")
        if ax is axes[0]:
            ax.set_ylabel("layer")
        last_im = im

    if last_im is not None:
        cbar = fig.colorbar(last_im, ax=axes[-1], fraction=0.04, pad=0.04)
        cbar.set_label(r"$\log_{2}$(per-token attn / $1/N$ ref.)", fontsize=9)
        cbar.ax.tick_params(labelsize=8)

    fig.tight_layout()
    fig.savefig(FIGS / "fig6_layerwise.png", dpi=160, bbox_inches="tight")
    fig.savefig(FIGS / "fig6_layerwise.pdf", bbox_inches="tight")
    plt.close(fig)
    print(f"  fig6 → {FIGS / 'fig6_layerwise.png'}")


def fig7_framepragm():
    fp_files = sorted((DATA / "frame_pragmatics").glob("frame_pragmatics__*.parquet"))
    if not fp_files:
        print("  no frame_pragmatics files; skipping fig7"); return
    fp = pd.concat([pd.read_parquet(f) for f in fp_files], ignore_index=True)
    classes = ["ORIG", "NEUTRAL", "ANAPHORIC", "NOVELTY"]
    labels = {"ORIG": "Topic-introducer (F0,F2)", "NEUTRAL": "Neutral / existential",
              "ANAPHORIC": "Anaphoric (demands repeat)", "NOVELTY": "Novelty (demands new word)"}
    colors = {"ORIG": "#1f77b4", "NEUTRAL": "#ff7f0e", "ANAPHORIC": "#2ca02c", "NOVELTY": "#d62728"}
    models = [(m, s) for (m, s, fam, c) in MECH_MODELS if m in fp["model"].unique()]
    fig, axes = plt.subplots(1, len(models), figsize=(15.4, 4.0), sharey=True, squeeze=False)
    for ax, (mid, short) in zip(axes[0], models):
        sub_m = fp[fp.model == mid]
        for cls in classes:
            sub = sub_m[sub_m.frame_class == cls]
            if sub.empty:
                continue
            mc = sub.groupby("N")["target_prob"].mean().reindex(NS)
            denom = mc.reindex(NS_NONZERO).max()
            ax.plot(NS, (mc / denom).values, marker="o", ms=4.5, lw=1.8,
                    color=colors[cls], label=labels[cls], zorder=3)
            pk = int(mc.reindex(NS_NONZERO).idxmax())
            ax.plot([pk], [mc[pk] / denom], marker="o", ms=7, mfc="white",
                    mec=colors[cls], mew=1.6, zorder=4)
        ax.axhline(1.0, ls=":", c="0.6", lw=0.8)
        ax.set_title(short)
        ax.set_xlabel("N (target repetitions)")
        ax.set_xticks([0, 5, 10, 15, 20, 25, 30])
        ax.grid(True, alpha=0.3)
        ax.set_ylim(-0.05, 1.08)
    axes[0][0].set_ylabel("peak-normalised mean $P$(target)")
    h, l = axes[0][0].get_legend_handles_labels()
    fig.legend(h, l, loc="lower center", ncol=4, frameon=True, framealpha=0.92,
               bbox_to_anchor=(0.5, -0.02), fontsize=9.5)
    fig.tight_layout(rect=[0, 0.07, 1, 1])
    fig.savefig(FIGS / "fig7_framepragm.png", dpi=160, bbox_inches="tight")
    fig.savefig(FIGS / "fig7_framepragm.pdf", bbox_inches="tight")
    plt.close(fig)
    print(f"  fig7 -> {FIGS / 'fig7_framepragm.png'}")


