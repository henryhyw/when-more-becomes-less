def fig5_mechanism():
    fig, axes = plt.subplots(1, 3, figsize=(15.4, 5.0))
    ax_a, ax_b, ax_c = axes

    for mid, short, fam, color in MECH_MODELS:
        attn_path = DATA / f"mechanism/attention_last_layer__{_safe(mid)}.csv"
        slot_path = DATA / f"mechanism/slot_state__{_safe(mid)}.csv"

        if attn_path.exists():
            a = pd.read_csv(attn_path)
            agg = (a.groupby("N").agg(per_token=("attn_per_target_token", "mean"),
                                       total=("attn_total_to_target_block", "mean"))
                    .reset_index().sort_values("N"))
            agg = agg[agg["N"] > 0]
            if not agg.empty and not np.isnan(agg["per_token"]).all():
                n0 = agg["N"].iloc[0]; v0 = agg["per_token"].iloc[0]
                ax_a.plot(agg["N"], agg["per_token"], marker="o", color=color,
                          linewidth=2.2, label=short)
                ref = v0 * (n0 / agg["N"].astype(float))
                ax_a.plot(agg["N"], ref, linestyle="--", color=color,
                          linewidth=1.5, alpha=0.95, dashes=(4, 3))
                ax_b.plot(agg["N"], agg["total"], marker="s", color=color,
                          linewidth=2.2, label=short)

        if slot_path.exists():
            s = pd.read_csv(slot_path)
            tp = s.groupby("N")["target_prob"].mean().sort_index()
            tp = tp[tp.index > 0]
            ax_c.plot(tp.index, tp.values, marker="o", color=color,
                      linewidth=2.2, label=short)

    ax_a.set_xlabel("repetitions in prefix $N$")
    ax_a.set_ylabel("per-target-token attention (log)")
    ax_a.set_title(r"(a) Per-token attention vs $1/N$ reference (dashed)")
    ax_a.set_yscale("log")
    ax_a.grid(True, alpha=0.3, which="both")

    ax_b.set_xlabel("repetitions in prefix $N$")
    ax_b.set_ylabel("total attention to repetition block")
    ax_b.set_title("(b) Total block budget vs $N$")
    ax_b.grid(True, alpha=0.3)

    ax_c.set_xlabel("repetitions in prefix $N$")
    ax_c.set_ylabel("mean P(target) (mechanism subset)")
    ax_c.set_title("(c) Slot-state target probability")
    ax_c.grid(True, alpha=0.3)

    legend_handles = [Line2D([0], [0], color=c, marker="o", linewidth=2.2, label=s)
                      for (_, s, _, c) in MECH_MODELS]
    ref_handle = Line2D([0], [0], color="grey", linestyle="--", linewidth=1.5,
                        dashes=(4, 3),
                        label=r"$1/N$ reference (dashed, panel a only)")
    fig.legend(handles=legend_handles + [ref_handle], loc="lower center",
               ncol=len(MECH_MODELS) + 1, fontsize=10, frameon=True,
               framealpha=0.92, bbox_to_anchor=(0.5, -0.04))
    fig.tight_layout(rect=[0, 0.04, 1, 1])
    fig.savefig(FIGS / "fig5_mechanism.png", dpi=160, bbox_inches="tight")
    fig.savefig(FIGS / "fig5_mechanism.pdf", bbox_inches="tight")
    plt.close(fig)
    print(f"  fig5 → {FIGS / 'fig5_mechanism.png'}")


